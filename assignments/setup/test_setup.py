def add(a, b):
    add=a+b
    return add

def subtract(a, b):
    subtract=a-b
    return subtract

def multiply(a, b):
    multiply=a*b
    return multiply

def divide(a, b):
    divide=a/b
    return divide

def power(a, b):
    power=a**b
    return power

def mod(a, b):
    mod=a % b
    return mod